void UpdateMixedBackground(unsigned short int OldPosX, unsigned short int OldPosY, unsigned short int NewPosX, unsigned short int NewPosY)
{
	char yloop, StartBufferOldXTemp=0, StartBufferOldYTemp=0, Position=0;
	unsigned char StartBufferNewX=0, StartBufferNewY=0, StartBufferOldX=0, StartBufferOldY=0, EndYLoop=0, EndXLoop=0;
	unsigned char StartBufferNewXTemp=0, StartBufferNewYTemp=0;
	unsigned short int ypos,i;
	signed char xloop;
	
	///Calcola differenza punti, ritorna 4 casi
	if((NewPosX<OldPosX)&&(NewPosY<=OldPosY))  Position=1;///alto a sinistra		
	if((NewPosX>OldPosX)&&(NewPosY<=OldPosY))  Position=2;///alto a destra		
	if((NewPosX<OldPosX)&&(NewPosY>=OldPosY))  Position=3;///basso a sinistra		
	if((NewPosX>=OldPosX)&&(NewPosY>=OldPosY))  Position=4;///basso a destra		
	if((NewPosX==OldPosX)&&(NewPosY>OldPosY))  Position=5;///salto basso 
	if((NewPosX==OldPosX)&&(NewPosY<OldPosY))  Position=6;///salto alto
	if((NewPosX==OldPosX)&&(NewPosY==OldPosY))  Position=7;///Nessun movimento
				
	switch(Position)
	{
		case 1:
			StartBufferNewX=OldPosX-NewPosX;
			StartBufferNewY=OldPosY-NewPosY;
			StartBufferOldX=0;
			StartBufferOldY=0;	
			
			EndYLoop=15-StartBufferNewY;
			EndXLoop=15-StartBufferNewX;
		break;
			
		case 2:
			StartBufferNewX=0;
			StartBufferNewY=OldPosY-NewPosY;
			StartBufferOldX=NewPosX-OldPosX;
			StartBufferOldY=0;
				
			EndYLoop=15-StartBufferNewY;
			EndXLoop=15-StartBufferOldX;
		break;
			
		case 3:
			StartBufferNewX=OldPosX-NewPosX;
			StartBufferNewY=0;
			StartBufferOldX=0;
			StartBufferOldY=NewPosY-OldPosY;	

			EndYLoop=15-StartBufferOldY;
			EndXLoop=15-StartBufferNewX;				
		break;
			
		case 4:
			StartBufferNewX=0;
			StartBufferNewY=0;
			StartBufferOldX=NewPosX-OldPosX;
			StartBufferOldY=NewPosY-OldPosY;

			EndYLoop=15-StartBufferOldY;
			EndXLoop=15-StartBufferOldX;				
		break;
			
		case 5:
			StartBufferNewX=0;
			StartBufferNewY=0;
			StartBufferOldX=0;
			StartBufferOldY=NewPosY-OldPosY;

			EndYLoop=15-StartBufferOldY;
			EndXLoop=15;				
		break;
			
		case 6:
			StartBufferNewX=0;
			StartBufferNewY=OldPosY-NewPosY;
			StartBufferOldX=0;
			StartBufferOldY=0;

			EndYLoop=15;
			EndXLoop=15;				
		break;
		
		case 7:
			StartBufferNewX=0;
			StartBufferNewY=0;
			StartBufferOldX=0;
			StartBufferOldY=0;
			EndXLoop=15;
			EndYLoop=15;
		break;
	}
	
	if((EndYLoop>200)||(EndXLoop>200));
	
	else
	{
		for(yloop=0; yloop<=EndYLoop; yloop++)
		{
			for(xloop=0; xloop<=EndXLoop; xloop++) BufferMixed[StartBufferNewY+yloop][StartBufferNewX+xloop]=BufferBackGround[StartBufferOldY+yloop][StartBufferOldX+xloop];		
		}
				
		//Stampa a video il vecchio background senza sovrapporre il nuovo backgr.
		
		i=OldPosX;
		OldPosX=OldPosY;
		OldPosY=i;
		OldPosX=239-OldPosX-15;
	
		//x=160 -> 184
		//y=40 -> 160
		ypos=OldPosY;
	
		SSD1289_writeResgiter( 0x004E, OldPosX );//la x di partenza
		SSD1289_writeResgiter( 0x004F, OldPosY );//la y di partenza
		SSD1289_sendCommand(0x0022); //set the RAM register for writing

		for(yloop=0; yloop<=15; yloop++)
		{
			for(xloop=15; xloop>=0; xloop--) SSD1289_sendData(BufferBackGround[xloop][yloop]);
			ypos++;
			SSD1289_writeResgiter( 0x004F, ypos );
			SSD1289_sendCommand(0x0022); //set the RAM register for writing
		}	
	}
}


